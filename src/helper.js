export function checkIfWin(idx, boxes, val){
  const groups = {
    0:[ [0,1,2], [0,4,8], [0,3,6]],
    1:[ [0,1,2], [1,4,7] ],
    2:[ [0,1,2], [2,5,8], [2,4,6] ],
    3:[ [3,4,5], [0,3,6] ],
    4:[ [3,4,5], [1,4,7], [0,4,8], [2,4,6] ],
    5:[ [3,4,5], [2,5,8] ],
    6:[ [0,3,6], [6,7,8], [2,4,6] ],
    7:[ [1,4,7], [6,7,8] ],
    8:[ [2,5,8], [6,7,8], [0,4,8] ],
  }
  return groups[idx].find(      
    arr=>arr.every(
      itemIdx=> itemIdx===idx || boxes[itemIdx] === val
    )
  ) || null
}